public class OrdenCarrera {
    private int primerC,segundoC,tercerC,cuartoC,quintoC,sextoC,septimoC,octavoC,novenoC,decimoC;

    public OrdenCarrera(int primerC, int segundoC, int tercerC, int cuartoC, int quintoC, int sextoC, int septimoC, int octavoC, int novenoC, int decimoC) {
        this.primerC = primerC;
        this.segundoC = segundoC;
        this.tercerC = tercerC;
        this.cuartoC = cuartoC;
        this.quintoC = quintoC;
        this.sextoC = sextoC;
        this.septimoC = septimoC;
        this.octavoC = octavoC;
        this.novenoC = novenoC;
        this.decimoC = decimoC;
    }

    public int getPrimerC() {
        return primerC;
    }

    public void setPrimerC(int primerC) {
        this.primerC = primerC;
    }

    public int getSegundoC() {
        return segundoC;
    }

    public void setSegundoC(int segundoC) {
        this.segundoC = segundoC;
    }

    public int getTercerC() {
        return tercerC;
    }

    public void setTercerC(int tercerC) {
        this.tercerC = tercerC;
    }

    public int getCuartoC() {
        return cuartoC;
    }

    public void setCuartoC(int cuartoC) {
        this.cuartoC = cuartoC;
    }

    public int getQuintoC() {
        return quintoC;
    }

    public void setQuintoC(int quintoC) {
        this.quintoC = quintoC;
    }

    public int getSextoC() {
        return sextoC;
    }

    public void setSextoC(int sextoC) {
        this.sextoC = sextoC;
    }

    public int getSeptimoC() {
        return septimoC;
    }

    public void setSeptimoC(int septimoC) {
        this.septimoC = septimoC;
    }

    public int getOctavoC() {
        return octavoC;
    }

    public void setOctavoC(int octavoC) {
        this.octavoC = octavoC;
    }

    public int getNovenoC() {
        return novenoC;
    }

    public void setNovenoC(int novenoC) {
        this.novenoC = novenoC;
    }

    public int getDecimoC() {
        return decimoC;
    }

    public void setDecimoC(int decimoC) {
        this.decimoC = decimoC;
    }
    
    
}
